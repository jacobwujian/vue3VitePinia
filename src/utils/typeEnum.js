export const EnumMap = (arr, key) => {
  if ((!arr || !key) && key !== 0) return null

  let map = new Map()
  arr.forEach(({ value, name }) => map.set(value, name))
  return map.get(key)
}

export const getCompanyType = companyType.map(province => ({
  label: province.label,
  value: province.value,
  children: province.children
}))

export const companyTypeCodeToText = str => {
  if (!str) return ''

  const code = str.split(',')
  const first = companyType.find(first => first.value === code[0])
  const next = first.children.find(next => next.value === code[1])

  if (first && next) {
    return `${first.label} - ${next.label}`
  }
}
